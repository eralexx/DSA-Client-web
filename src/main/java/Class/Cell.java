package Class;

public class Cell {

     int CellType;
     int PosX;
     int PosY;

     public int getCellType() {
          return CellType;
     }

     public void setCellType(int cellType) {
          CellType = cellType;
     }

     public int getPosX() {
          return PosX;
     }

     public void setPosX(int posX) {
          PosX = posX;
     }

     public int getPosY() {
          return PosY;
     }

     public void setPosY(int posY) {
          PosY = posY;
     }
}
